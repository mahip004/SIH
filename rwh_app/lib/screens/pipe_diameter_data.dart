  PipeData findClosest(double roofArea, int rainfallIntensity) {
    PipeData? closest;
    double minDistance = double.infinity;

    for (var data in dataset) {
      double distance = sqrt(
        pow(data.roofArea - roofArea, 2) +
            pow(data.rainfallIntensity - rainfallIntensity, 2),
      );

      if (distance < minDistance) {
        minDistance = distance;
        closest = data;
      }
    }

    return closest!;
  }
